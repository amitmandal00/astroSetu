/**
 * Get image URL for a specific category and type
 */
export function getAstroImage(category: keyof typeof ASTRO_IMAGES, type?: string): string {
  const categoryImages = ASTRO_IMAGES[category];
  
  if (typeof categoryImages === 'string') {
    return categoryImages;
  }
  
  if (type && type in categoryImages) {
    return categoryImages[type as keyof typeof categoryImages] as string;
  }
  
  // Return first available image or fallback
  const firstKey = Object.keys(categoryImages)[0];
  return (categoryImages as any)[firstKey] || ASTRO_IMAGES.fallback;
}

/**
 * Get remedy image by type
 */
export function getRemedyImage(type: string): string {
  const typeLower = type.toLowerCase();
  const remedyMap: Record<string, keyof typeof ASTRO_IMAGES.remedies> = {
    gemstone: 'gemstone',
    mantra: 'mantra',
    yantra: 'yantra',
    puja: 'puja',
    ritual: 'ritual',
  };
  
  const remedyType = remedyMap[typeLower] || 'default';
  return ASTRO_IMAGES.remedies[remedyType];
}

/**
 * Get zodiac sign image
 */
export function getZodiacImage(sign: string): string {
  const signLower = sign.toLowerCase();
  const signKey = signLower as keyof typeof ASTRO_IMAGES.zodiac;
  return ASTRO_IMAGES.zodiac[signKey] || ASTRO_IMAGES.fallback;
}

